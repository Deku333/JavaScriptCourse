// Não podemos criar variáveis com palavras reservadas por exemplo: if, try, else, let
// Variáveis precisam ter nomes significativos
// Não comece variáveis com números
// Nomes de variáveis não podem conter traços nem espaços
// Utilizamos camelCase
// Case-sensitive, ou seja, letra maiúsculas e minúsculas são diferentes
// Não podemos re-declarar variáveis com let
// Não use var, use let

let nome = "João";

console.log(`${nome} nasceu em 1904.`);
console.log(`Em 2000 ${nome} conheceu Maria.`);
console.log(`${nome} casou-se com Maria em 2012.`);
console.log(`Maria teve 1 filho com ${nome} em 2015.`);
console.log(`O filho de ${nome} se chama Eduardo`);

