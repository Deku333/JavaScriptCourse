// Não podemos criar constantes com palavras reservadas por exemplo: if, try, else, let
// Constantes precisam ter nomes significativos
// Não comece constantes com números
// Nomes de constantes não podem conter traços nem espaços
// Utilizamos camelCase
// Case-sensitive, ou seja, letra maiúsculas e minúsculas são diferentes
// Não podemos modificar o valor de uma constante
// Não use var, use const

const firstNumber = 5;
const secondNumber = 10;
const result = firstNumber * secondNumber;
const duplicateResult = result * 2;
let triplicateResult = result * 3;
triplicateResult = triplicateResult + 5;
